a = [21, 2, 31, 42, 53, 67, 72, 18, 93, 11, 'яблуко', 'груша', 'апельсин', 'слива', 'манго', 'виноград',  'вишня',  'ананас', 'ківі', 'персик'] 
# Основний список, який містить як числа, так і слова 
 
nums = []  # список для чисел 
words = [] # список для слів 
 
for el in a:  # проходимо по кожному елементу списку 'a' 
    if type(el) == int:  # якщо елемент є числом 
        nums.append(el)  # додаємо його до списку чисел 
    else:                # якщо елемент не число 
        words.append(el) # додаємо його до списку слів 
 
nums.sort()  # сортуємо числа за зростанням 
words.sort() # сортуємо слова в алфавітному порядку 
 
list = nums + words  # об’єднуємо два списки в один 
 
even_list = []  # список для парних чисел 
for n in nums:  # проходимо по всіх числах 
    if n % 2 == 0:        # перевіряємо, чи число парне 
        even_list.append(n)  # додаємо його в список парних 
 
caps_list = []  # список для слів у верхньому регістрі 
for w in words:               # проходимо по кожному слову 
    caps_list.append(w.upper())  # перетворюємо слово на великі літери 
 
print("Перший список:", a)             # виводимо початковий список 
print("Відсортований список:", list)   # виводимо об’єднаний відсортований список 
print("Парні числа:", even_list)       # виводимо парні числа 
print("Капс:", caps_list)              # виводимо список слів у верхньому регістрі
